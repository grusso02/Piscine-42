char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int j;

	if (!*to_find)
		return (str);
	i = 0;
	while (str[i])
	{
		j = 0;
		while (to_find[j] == str[i + j])
		{
			if (!to_find[j + 1])
			{
				return (str + i);
			}
			j++;
		}
		i++;
	}
	return (0);
}
